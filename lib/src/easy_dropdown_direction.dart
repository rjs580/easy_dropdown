/// The EasyDropdownDirection enum specifies where the dropdown aligns in
/// relation to the button. The options include 'above', 'below', and 'auto'.
enum EasyDropdownDirection {
  /// The dropdown appears on top of the child.
  above,

  /// The dropdown appears underneath the child.
  below,

  /// The dropdown position is determined automatically based on the context and
  /// space available.
  auto,
}
